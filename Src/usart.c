int fputc (int ch,FILE *f)
{
	HAL_UART_Transmit(&huart3,(uint8_t*)&ch,1,0xFFFF);
	return (ch);
}

int fgetc(FILE *f)
{
	int ch;
	while(__HAL_UART_GET_FLAG(&huart3,UART_FLAG_RXNE) == RESET);
	HAL_UART_Receive(&huart3,(uint8_t *)&ch,1,0xFFFF);
	return ch;
}
